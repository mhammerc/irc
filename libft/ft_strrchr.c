#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = ft_strlen(s);
	while (i != -1)
	{
		if (s[i] == (char)c)
			return ((char*)(s) + sizeof(char) * i);
		--i;
	}
	return (NULL);
}
